# Lambda Function are small anonymous functions that can be defined using the lambda keyword.
# They can have any number of arguments and a single expression as their body.

# Example of a simple lambda function in Python
add_ten = lambda x: x + 10

# Example usage
result = add_ten(5)
print(result)  # Output: 15


# Example 2 of a simple lambda function in Python

even = lambda x: x % 2 == 0

# Example usage
result = even(5)
print(result)  # Output: False



#Example 3 of a simple lambda function in Python

add = lambda x, y , z : x + y + z

# Example usage

result = add(1, 2, 3)
print(result)

